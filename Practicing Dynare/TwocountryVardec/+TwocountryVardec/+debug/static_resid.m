function [lhs, rhs] = static_resid(y, x, params)
T = NaN(8, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = exp(y(1))^(-params(1));
T(2) = params(3)*exp(y(5))*exp(y(3))^(params(3)-1);
T(3) = 1+T(2)-params(2);
T(4) = exp(y(2))^(-params(1));
T(5) = params(3)*exp(y(6))*exp(y(4))^(params(3)-1);
T(6) = 1+T(5)-params(2);
T(7) = exp(y(5))*exp(y(3))^params(3);
T(8) = exp(y(6))*exp(y(4))^params(3);
lhs(1) = y(1);
rhs(1) = y(2);
lhs(2) = T(1);
rhs(2) = T(1)*params(4)*T(3);
lhs(3) = T(4);
rhs(3) = params(4)*T(4)*T(6);
lhs(4) = exp(y(4))+exp(y(3))+exp(y(1))+exp(y(2))-exp(y(3))*(1-params(2))-exp(y(4))*(1-params(2));
rhs(4) = T(7)+T(8);
lhs(5) = y(5);
rhs(5) = y(5)*params(5)+x(1);
lhs(6) = y(6);
rhs(6) = y(6)*params(5)+x(2);
lhs(7) = exp(y(7));
rhs(7) = T(7);
lhs(8) = exp(y(8));
rhs(8) = T(8);
end
